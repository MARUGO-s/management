        // correction.htmlページにリダイレクト（修正版）
        redirectToCorrectionPage(selectedData) {
            try {
                console.log('=== リダイレクト処理開始 ===');
                console.log('選択されたデータ:', selectedData);
                
                // データの検証
                if (!selectedData) {
                    throw new Error('送信するデータが無効です');
                }
                
                // 必要なフィールドの検証
                const requiredFields = ['date', 'lender', 'borrower', 'category', 'item', 'amount', 'originalRowIndex']; // originalRowIndex を追加
                const missingFields = requiredFields.filter(field => !selectedData[field]);
                if (missingFields.length > 0) {
                    console.warn('不足しているフィールド:', missingFields);
                }
                
                // sessionStorageに選択されたデータを保存
                const dataToSave = JSON.stringify(selectedData);
                console.log('保存するデータ:', dataToSave);
                
                sessionStorage.setItem('correctionData', dataToSave);
                console.log('sessionStorageへの保存完了');
                
                // 保存されたデータを確認
                const savedData = sessionStorage.getItem('correctionData');
                console.log('保存確認:', savedData ? '成功' : '失敗');
                
                if (!savedData) {
                    throw new Error('sessionStorageへの保存に失敗しました');
                }
                
                // URLを決定（現在のページの場所に基づいて適切なパスを設定）
                let correctionUrl;
                const currentPath = window.location.pathname;
                console.log('現在のパス:', currentPath);
                
                if (currentPath.includes('/data/')) {
                    // data/marugo.html から呼び出された場合
                    correctionUrl = '../correction.html';
                } else if (currentPath.endsWith('/')) {
                    // ルートディレクトリから呼び出された場合
                    correctionUrl = 'correction.html';
                } else {
                    // その他の場合
                    correctionUrl = 'correction.html';
                }
                
                console.log('リダイレクト先URL:', correctionUrl);
                
                // バックアップとして、localStorageにも保存
                try {
                    localStorage.setItem('correctionData', dataToSave);
                    console.log('localStorageにもバックアップ保存完了');
                } catch (localStorageError) {
                    console.warn('localStorageへのバックアップ保存に失敗:', localStorageError);
                }
                
                // correction.htmlページに移動
                window.location.href = correctionUrl;
                
            } catch (error) {
                console.error('リダイレクトエラー:', error);
                // alert(`修正ページへの移動中にエラーが発生しました: ${error.message}`); // alert の代わりにカスタムモーダルを使用
                this.showCustomAlertDialog(`修正ページへの移動中にエラーが発生しました: ${error.message}`);
                
                // デバッグ情報をコンソールに出力
                console.log('エラー時のデバッグ情報:');
                console.log('- 選択されたデータ:', selectedData);
                console.log('- 現在のURL:', window.location.href);
                console.log('- sessionStorage状態:', Object.keys(sessionStorage));
                console.log('- localStorage状態:', Object.keys(localStorage));
            }
        }

        exportResults() {
            if (this.searchResults.length === 0) {
                // alert('エクスポートするデータがありません'); // alert の代わりにカスタムモーダルを使用
                this.showCustomAlertDialog('エクスポートするデータがありません');
                return;
            }

            // CSV出力用のマッピング (device情報を除く)
            const csvData = Papa.unparse(this.searchResults.map(row => ({
                日付: row.date,
                取引名: row.name,
                貸主: row.lender,
                借主: row.borrower,
                カテゴリー: row.category,
                品目: row.item,
                金額: row.amount,
                入力日時: row.inputDate,
                修正: row.correction || ''
            })));

            const blob = new Blob([csvData], { type: 'text/csv;charset=utf-8;' });
            const link = document.createElement('a');
            const url = URL.createObjectURL(blob);
            link.setAttribute('href', url);
            link.setAttribute('download', `検索結果_${new Date().toISOString().split('T')[0]}.csv`);
            link.style.visibility = 'hidden';
            document.body.appendChild(link);
            link.click();
            document.body.removeChild(link);

            this.log('CSV出力完了', 'success');
        }
